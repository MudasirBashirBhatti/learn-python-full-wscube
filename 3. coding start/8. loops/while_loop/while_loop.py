# while loop manage conditions just like if else
# while loop will run until condition is false.

i=1                     #start
while i<=5:             #condition
    i=i+1               #increment or decrement(if not write this it will be infinite loop)
    print('hello')      #statement

print(i)
# in while loop i++ or i-- will not work here you can do i+1

# reverse loop 
a = 5
while a>=0:
    a=a-1
    print('value is',a)