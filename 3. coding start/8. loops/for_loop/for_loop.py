range(5)
# above statement means
# start point 0
# end point < 5
# increment +1
# return 0 1 2 3 4
print("..........range(5).........")
for n in range(5):
    print(n)





range(2,5)
# start point 2
# end point < 5
# increment +1
# return 2 3 4
print("..........range(2,5).........")
for n in range(2,5):
    print(n)





range(1,10,2)
# start point 2
# end point < 5
# increment +3
# return 1 3 5 7 9 
print("..........range(2,5).........")
for n in range(1,10, 2):
    print(n)



